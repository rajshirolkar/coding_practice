class Solution:
    def integerBreak(self, n: int) -> int:
        """
            2: 1 + 1 -> 1*1 = 1
            3: 1 + 2 -> 1*2 = 2
            4: 2 + 2 -> 2*2 = 4
            5: 2 + 3 -> 2*3 = 6
            6: 3 + 3 -> 3*3 = 9
            7: 3 + 4 -> 3*4 = 12
            8: 4 + 4 -> 4*4 = 16
            9: 3 + 3 + 3 -> 3*3*3 = 27
            10: 3 + 3 + 4 -> 3*3*4 = 36
            11: 2 + 3 + 3 + 3 -> 3*3*3*2 = 54
            12: 3 + 3 + 3 + 3 -> 3*3*3*3 = 81
            20: 2 + 3 + 3 + 3 + 3 + 3 + 3 = 1458
        """
        if n == 2:
            return 1
        if n == 3:
            return 2
        q = n // 3
        r = n % 3
        if r == 0:
            return 3**q
        elif r == 1:
            return (3**(q-1))*4
        else:
            return (3**q)*2